public class fraction {
    int numer;
    int denom;
    public fraction(int n, int d)
    {
        numer = n;
        denom = d;
    }

    public int getNumer()
    {
        return numer;
    }

    public int getDenom()
    {
        return denom;
    }

    public void setNumer(int n)
    {
        numer=n;
    }
    public void setDenom(int d)
    {
        denom=d;
    }

    
    

}
